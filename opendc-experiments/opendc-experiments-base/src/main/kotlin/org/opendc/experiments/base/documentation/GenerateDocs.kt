/**
 * Generates the Markdown reference documentation for OpenDC's experiment input
 * format and Parquet output columns, straight from the code that is the source
 * of truth for both.
 *
 * The input tables are derived from the [kotlinx.serialization] descriptors of
 * [ExperimentSpec] and everything it (transitively) references. The output
 * tables are derived from [ComputeExportConfig.ALL_COLUMNS].
 *
 * Human-written prose is merged in from optional JSON side-car files
 * (`descriptions/input.json` and `descriptions/output.json`) so that
 * regenerating never clobbers hand-written descriptions.
 *
 * Usage: `GenerateDocs <docsRootDir>`
 */
public fun main(args: Array<String>) {
    val docsRoot = File(args.getOrElse(0) { "docs" })
    val specsSource = args.getOrNull(1)?.let(::File)
    val referenceDir = docsRoot.resolve("reference").apply { mkdirs() }
    val descriptionsDir = docsRoot.resolve("descriptions").apply { mkdirs() }

    // Descriptions are layered: KDoc harvested from the spec source is the base layer,
    // and the JSON side-car overrides it for anything not (or not well) expressed in code.
    val inputDescriptions = parseKdoc(specsSource) + loadDescriptions(descriptionsDir.resolve("input.json"))
    val outputDescriptions = loadDescriptions(descriptionsDir.resolve("output.json"))
    val layout = loadLayout(docsRoot.resolve("reference-layout.json"))
    val sourceOrder = parseSourceOrder(specsSource)

    val inputFiles = renderInput(inputDescriptions, layout, sourceOrder)

    // Remove previously generated files (and now-empty folders) so renames/regroupings
    // do not leave stale pages behind. Only files carrying the generated header are touched.
    cleanGeneratedFiles(referenceDir)

    for ((fileName, content) in inputFiles) {
        val file = referenceDir.resolve(fileName)
        file.parentFile?.mkdirs()
        file.writeText(content)
        println("Wrote $file")
    }

    val outputFile = referenceDir.resolve("output-columns.md")
    outputFile.writeText(renderOutput(outputDescriptions))
    println("Wrote $outputFile")
}

private const val GENERATED_HEADER =
    "<!--\n" +
        "  DO NOT EDIT. This file is generated by `./gradlew generateDocs`.\n" +
        "  Field names, types and columns come from the source code; edit prose in\n" +
        "  docs/descriptions/*.json and regenerate.\n" +
        "-->\n\n"

private const val DEFAULT_INPUT_FILE = "experiment-input.md"

private val json = Json { ignoreUnknownKeys = true }

private fun loadDescriptions(file: File): Map<String, String> =
    if (file.exists()) json.decodeFromString<Map<String, String>>(file.readText()) else emptyMap()

/**
 * Deletes previously generated Markdown under [referenceDir] (identified by the generated header)
 * and any directories left empty afterwards. Hand-written files without the header are left alone.
 */
private fun cleanGeneratedFiles(referenceDir: File) {
    if (!referenceDir.exists()) return
    referenceDir
        .walkTopDown()
        .filter { it.isFile && it.extension == "md" && it.readText().startsWith(GENERATED_HEADER) }
        .forEach { it.delete() }
    referenceDir
        .walkBottomUp()
        .filter { it.isDirectory && it != referenceDir && it.listFiles().isNullOrEmpty() }
        .forEach { it.delete() }
}

/**
 * Optional layout that splits the input reference across several Markdown files.
 *
 * Each [LayoutFile] lists the types it owns (by display name); listing a sealed parent pulls in
 * its variants automatically. Types not claimed by any file fall into [defaultFile] (or the first
 * file). When no layout file is present everything ends up in a single [DEFAULT_INPUT_FILE].
 */
@Serializable
private data class ReferenceLayout(
    val defaultFile: String? = null,
    val files: List<LayoutFile> = emptyList(),
)

@Serializable
private data class LayoutFile(
    val file: String,
    val title: String? = null,
    val types: List<String> = emptyList(),
)

private fun loadLayout(file: File): ReferenceLayout =
    if (file.exists()) json.decodeFromString<ReferenceLayout>(file.readText()) else ReferenceLayout()

// ---------------------------------------------------------------------------
// Input (experiment JSON) generation
// ---------------------------------------------------------------------------

/** A structured type discovered in the descriptor tree that gets its own Markdown section. */
private class TypeNode(
    val descriptor: SerialDescriptor,
    val key: String,
    val parent: TypeNode?,
) {
    val variants: MutableList<TypeNode> = mutableListOf()

    /** Heading text; sealed variants are qualified with their parent to stay unique. */
    val display: String =
        if (parent != null) "${simpleName(parent.descriptor)}.${simpleName(descriptor)}" else simpleName(descriptor)

    val anchor: String = slug(display)
}

/**
 * Walks the serialization descriptor tree once, recording every structured type
 * in discovery order together with the polymorphic (sealed) relationships, then
 * renders one Markdown section per type.
 *
 * Types are deduplicated by a stable string key (kotlinx does not guarantee a
 * single descriptor instance per type). Sealed variants are keyed by their
 * parent as well, because several families reuse the same `@SerialName`.
 */
private fun renderInput(
    descriptions: Map<String, String>,
    layout: ReferenceLayout,
    sourceOrder: SourceOrder,
): Map<String, String> {
    val nodes = LinkedHashMap<String, TypeNode>()

    fun keyOf(
        descriptor: SerialDescriptor,
        parent: TypeNode?,
    ): String = if (parent != null) "${parent.key}/${descriptor.serialName}" else descriptor.serialName

    fun collect(
        descriptor: SerialDescriptor,
        parent: TypeNode?,
    ) {
        // Unwrap collections; the container itself never gets its own section.
        when (descriptor.kind) {
            StructureKind.LIST, StructureKind.MAP -> {
                for (i in 0 until descriptor.elementsCount) collect(descriptor.getElementDescriptor(i), null)
                return
            }
            SerialKind.ENUM, is PolymorphicKind, StructureKind.CLASS, StructureKind.OBJECT -> {}
            else -> return // primitives / contextual: no section of their own
        }

        val key = keyOf(descriptor, parent)
        if (nodes.containsKey(key)) return
        val node = TypeNode(descriptor, key, parent)
        nodes[key] = node

        when (descriptor.kind) {
            is PolymorphicKind -> {
                for (i in 0 until descriptor.elementsCount) {
                    val holder = descriptor.getElementDescriptor(i)
                    for (j in 0 until holder.elementsCount) {
                        val variant = holder.getElementDescriptor(j)
                        collect(variant, node)
                        nodes[keyOf(variant, node)]?.let { if (it !in node.variants) node.variants.add(it) }
                    }
                }
            }
            StructureKind.CLASS, StructureKind.OBJECT ->
                for (i in 0 until descriptor.elementsCount) collect(descriptor.getElementDescriptor(i), null)
            else -> {} // enums have no children to recurse into
        }
    }

    collect(ExperimentSpec.serializer().descriptor, null)

    // Assign every type to an output file, then render one file per group.
    val fileOf = assignFiles(nodes.values, layout)
    val grouped = LinkedHashMap<String, MutableList<TypeNode>>()
    layout.files.forEach { grouped.getOrPut(it.file) { mutableListOf() } }
    for (node in nodes.values) grouped.getOrPut(fileOf.getValue(node.display)) { mutableListOf() }.add(node)

    // Present types in source order instead of descriptor-discovery order. Within a page, types from
    // the same source file are kept together (source files ordered by when the descriptor walk first
    // reaches them, so the root spec's file leads) and ordered by their declaration index, which puts
    // a sealed parent right before its variants. Types with no source (external enums) sort last, in
    // discovery order, so links to them still resolve.
    val discoveryIndex = nodes.values.withIndex().associate { (index, node) -> node.display to index }
    val fileRank = HashMap<String, Int>()
    for (node in nodes.values) {
        val sourceFile = sourceOrder.fileOf[node.display] ?: continue
        fileRank.merge(sourceFile, discoveryIndex.getValue(node.display), ::minOf)
    }
    val sortKey: (TypeNode) -> Pair<Int, Int> = { node ->
        val sourceFile = sourceOrder.fileOf[node.display]
        if (sourceFile != null) {
            fileRank.getValue(sourceFile) to (sourceOrder.indexInFile[node.display] ?: 0)
        } else {
            (nodes.size + discoveryIndex.getValue(node.display)) to 0
        }
    }
    grouped.values.forEach { fileNodes -> fileNodes.sortWith(compareBy({ sortKey(it).first }, { sortKey(it).second })) }

    val result = LinkedHashMap<String, String>()
    for ((fileName, fileNodes) in grouped) {
        if (fileNodes.isEmpty()) continue
        val href: (String) -> String = { display -> hrefFor(display, fileName, fileOf) }

        val sb = StringBuilder(GENERATED_HEADER)
        sb.appendLine("# ${titleFor(fileName, layout)}")
        sb.appendLine()
        if (fileNodes.any { it.display == "ExperimentSpec" }) {
            sb.appendLine(
                "An experiment is described by a single JSON file. Its top-level object is the " +
                    "[ExperimentSpec](${href("ExperimentSpec")}); every axis listed there forms a cartesian product of " +
                    "scenarios. Topology files are referenced by path and documented separately.",
            )
            sb.appendLine()
        }
        for (node in fileNodes) {
            when (node.descriptor.kind) {
                SerialKind.ENUM -> renderEnum(sb, node, descriptions)
                is PolymorphicKind -> renderPolymorphic(sb, node, descriptions, href)
                else -> renderClass(sb, node, descriptions, href)
            }
        }
        result[fileName] = sb.toString()
    }
    return result
}

/** Maps every type's display name to the output file it belongs in, honouring [layout]. */
private fun assignFiles(
    nodes: Collection<TypeNode>,
    layout: ReferenceLayout,
): Map<String, String> {
    val defaultFile = layout.defaultFile ?: layout.files.firstOrNull()?.file ?: DEFAULT_INPUT_FILE
    val byDisplay = nodes.associateBy { it.display }
    val fileOf = LinkedHashMap<String, String>()
    for (layoutFile in layout.files) {
        for (typeName in layoutFile.types) {
            val node = byDisplay[typeName] ?: continue
            // A sealed parent drags its variants into the same file.
            if (fileOf.putIfAbsent(node.display, layoutFile.file) == null) {
                for (variant in node.variants) fileOf.putIfAbsent(variant.display, layoutFile.file)
            }
        }
    }
    for (node in nodes) fileOf.putIfAbsent(node.display, defaultFile)
    return fileOf
}

/**
 * A Markdown href to [display]'s section: a bare anchor within [currentFile], otherwise the target
 * file relative to the current file's directory (so links work across sub-folders) plus the anchor.
 */
private fun hrefFor(
    display: String,
    currentFile: String,
    fileOf: Map<String, String>,
): String {
    val anchor = "#${slug(display)}"
    val target = fileOf[display]
    if (target == null || target == currentFile) return anchor
    val fromDir = File(currentFile).parentFile
    val relative = if (fromDir == null) target else fromDir.toPath().relativize(File(target).toPath()).toString()
    return "${relative.replace(File.separatorChar, '/')}$anchor"
}

private fun titleFor(
    file: String,
    layout: ReferenceLayout,
): String =
    layout.files.firstOrNull { it.file == file }?.title
        ?: if (file == DEFAULT_INPUT_FILE) {
            "Experiment input format"
        } else {
            File(file).name.removeSuffix(".md").replace('-', ' ').replaceFirstChar { it.uppercase() }
        }

/**
 * Markdown heading line for a type's section. A sealed variant renders one level deeper than a
 * top-level type (`###` vs `##`), so each page reads as a hierarchy beneath its `#` title.
 */
private fun heading(node: TypeNode): String {
    val hashes = if (node.parent != null) "###" else "##"
    return "$hashes ${node.display} { #${node.anchor} }"
}

private fun renderClass(
    sb: StringBuilder,
    node: TypeNode,
    descriptions: Map<String, String>,
    href: (String) -> String,
) {
    val descriptor = node.descriptor
    sb.appendLine(heading(node))
    sb.appendLine()
    node.parent?.let { parent ->
        sb.appendLine(
            "Variant of [${parent.display}](${href(parent.display)}), " +
                "selected by setting `type` to `\"${simpleName(descriptor)}\"`.",
        )
        sb.appendLine()
    }
    descriptions[node.display]?.let {
        sb.appendLine(it)
        sb.appendLine()
    }
    if (descriptor.elementsCount == 0) {
        sb.appendLine("_No configurable fields._")
        sb.appendLine()
        return
    }
    sb.appendLine("| Field | Type | Required | Description |")
    sb.appendLine("|-------|------|----------|-------------|")
    for (i in 0 until descriptor.elementsCount) {
        val name = descriptor.getElementName(i)
        val type = renderType(descriptor.getElementDescriptor(i), href)
        val required = if (descriptor.isElementOptional(i)) "no" else "**yes**"
        val desc = cell(descriptions["${node.display}.$name"])
        sb.appendLine("| `$name` | $type | $required | $desc |")
    }
    sb.appendLine()
}

private fun renderPolymorphic(
    sb: StringBuilder,
    node: TypeNode,
    descriptions: Map<String, String>,
    href: (String) -> String,
) {
    sb.appendLine(heading(node))
    sb.appendLine()
    descriptions[node.display]?.let {
        sb.appendLine(it)
        sb.appendLine()
    }
    sb.appendLine("Polymorphic type. Set the `type` field to select one of the following variants:")
    sb.appendLine()
    for (variant in node.variants) {
        sb.appendLine("- `${simpleName(variant.descriptor)}` → [${variant.display}](${href(variant.display)})")
    }
    sb.appendLine()
}

private fun renderEnum(
    sb: StringBuilder,
    node: TypeNode,
    descriptions: Map<String, String>,
) {
    val descriptor = node.descriptor
    sb.appendLine(heading(node))
    sb.appendLine()
    descriptions[node.display]?.let {
        sb.appendLine(it)
        sb.appendLine()
    }
    sb.appendLine("One of:")
    sb.appendLine()
    for (i in 0 until descriptor.elementsCount) {
        val value = descriptor.getElementName(i)
        val desc = descriptions["${node.display}.$value"]?.let { " — $it" } ?: ""
        sb.appendLine("- `$value`$desc")
    }
    sb.appendLine()
}

private fun renderType(
    descriptor: SerialDescriptor,
    href: (String) -> String,
): String {
    val rendered =
        when (descriptor.kind) {
            PrimitiveKind.BOOLEAN -> "Boolean"
            PrimitiveKind.BYTE, PrimitiveKind.SHORT, PrimitiveKind.INT -> "Int"
            PrimitiveKind.LONG -> "Long"
            PrimitiveKind.FLOAT -> "Float"
            PrimitiveKind.DOUBLE -> "Double"
            PrimitiveKind.CHAR, PrimitiveKind.STRING -> "String"
            // Sets and Lists both serialize to a JSON array (StructureKind.LIST); the
            // collection's serialName is what distinguishes them. Angle brackets are emitted
            // as HTML entities so Markdown does not swallow them as tags.
            StructureKind.LIST -> {
                val collection = if (descriptor.serialName.substringAfterLast('.').contains("Set")) "Set" else "List"
                "$collection&lt;${renderType(descriptor.getElementDescriptor(0), href)}&gt;"
            }
            StructureKind.MAP ->
                "Map&lt;${renderType(descriptor.getElementDescriptor(0), href)}, " +
                    "${renderType(descriptor.getElementDescriptor(1), href)}&gt;"
            SerialKind.ENUM, is PolymorphicKind, StructureKind.CLASS, StructureKind.OBJECT -> {
                val name = simpleName(descriptor)
                "[$name](${href(name)})"
            }
            else -> simpleName(descriptor)
        }
    return if (descriptor.isNullable) "$rendered?" else rendered
}

// ---------------------------------------------------------------------------
// Output (Parquet columns) generation
// ---------------------------------------------------------------------------

private fun renderOutput(descriptions: Map<String, String>): String {
    val config = ComputeExportConfig.ALL_COLUMNS
    val tables =
        listOf(
            "host" to config.hostExportColumns,
            "task" to config.taskExportColumns,
            "powerSource" to config.powerSourceExportColumns,
            "battery" to config.batteryExportColumns,
            "service" to config.serviceExportColumns,
        )

    val sb = StringBuilder(GENERATED_HEADER)
    sb.appendLine("# Output columns")
    sb.appendLine()
    sb.appendLine(
        "Each simulation run writes one Parquet file per output type under " +
            "`output/raw-output/<scenarioId>/seed=<seed>/`. The columns of each file are listed below. " +
            "Which files and columns are written is controlled by the `exportModels` axis of the experiment.",
    )
    sb.appendLine()

    for ((name, columns) in tables) {
        sb.appendLine("## `$name.parquet` { #$name }")
        sb.appendLine()
        sb.appendLine("| Column | Type | Description |")
        sb.appendLine("|--------|------|-------------|")
        for (column in columns.sortedBy { it.name }) {
            val desc = cell(descriptions["$name.${column.name}"])
            sb.appendLine("| `${column.name}` | ${parquetType(column)} | $desc |")
        }
        sb.appendLine()
    }
    return sb.toString()
}

private fun parquetType(column: ExportColumn<*>): String = column.primitiveTypeName.name

