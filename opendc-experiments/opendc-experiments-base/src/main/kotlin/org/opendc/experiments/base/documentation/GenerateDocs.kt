/** The short type name, stripping the package and any nullability marker. */
private fun simpleName(descriptor: SerialDescriptor): String = descriptor.serialName.removeSuffix("?").substringAfterLast('.')

/** Deterministic anchor slug matching the explicit `{ #slug }` we emit on headings. */
private fun slug(display: String): String =
    display
        .lowercase()
        .map { if (it.isLetterOrDigit()) it else '-' }
        .joinToString("")
        .trim('-')
        .replace(Regex("-+"), "-")

/** Renders text into a single Markdown table cell (escapes pipes, blank when absent). */
private fun cell(text: String?): String = text?.replace("|", "\\|").orEmpty()

// ---------------------------------------------------------------------------
// KDoc harvesting (Option A: parse the spec source)
// ---------------------------------------------------------------------------

/** A single type declaration found in the spec source, in the order it appears. */
private class SourceDecl(
    val display: String,
    val kdoc: String,
    val file: String,
    val indexInFile: Int,
)

/**
 * Scans the `.kt` files under [root] and returns every type declaration in source order.
 *
 * Files are visited in a stable (path-sorted) order and, within each file, declarations are returned
 * in the order they appear. The [SourceDecl.display] matches the display names used by [renderInput]:
 * the simple class name, or `"<Parent>.<serialName>"` for a sealed variant.
 */
private fun scanDeclarations(root: File?): List<SourceDecl> {
    if (root == null || !root.exists()) return emptyList()
    val files = root.walkTopDown().filter { it.isFile && it.extension == "kt" }.sortedBy { it.path }.toList()
    val texts = files.map { it.readText() }

    val sealedBases =
        texts
            .flatMap { Regex("""sealed\s+(?:class|interface)\s+(\w+)""").findAll(it).map { m -> m.groupValues[1] } }
            .toSet()

    // Matches an optional KDoc block, any annotations, and the type declaration that follows.
    // Capture groups: 1 = KDoc block, 2 = annotations, 3 = type name.
    //
    // The KDoc body uses the standard chunk-based comment pattern (`[^*]*\*+(?:[^/*][^*]*\*+)*`)
    // rather than `[\s\S]*?`: it ends at the comment's own first `*/` and can never span across
    // intervening code (e.g. a documented member of a sealed type sitting between the parent's KDoc
    // and its first variant) into the next comment. It also matches linearly, so it does not blow the
    // stack on long comments the way an alternation-in-a-quantifier would.
    val declRegex =
        Regex(
            """(?:(/\*\*[^*]*\*+(?:[^/*][^*]*\*+)*/)\s*)?""" +
                """((?:@\w+(?:\([^)]*\))?\s*)*)""" +
                """(?:public\s+|internal\s+|private\s+)?(?:sealed\s+)?(?:abstract\s+)?(?:data\s+)?(?:enum\s+)?""" +
                """(?:class|interface|object)\s+(\w+)""",
        )

    val result = mutableListOf<SourceDecl>()
    for ((fileIndex, text) in texts.withIndex()) {
        var indexInFile = 0
        for (match in declRegex.findAll(text)) {
            val kdoc = match.groupValues[1]
            val annotations = match.groupValues[2]
            val name = match.groupValues[3]

            val serialName = Regex("""@SerialName\("([^"]*)"\)""").find(annotations)?.groupValues?.get(1) ?: name
            val serialSimple = serialName.substringAfterLast('.')
            val parent = extractSupertypes(text.substring(match.range.last + 1)).firstOrNull { it in sealedBases }
            val display = if (parent != null) "$parent.$serialSimple" else serialSimple

            result.add(SourceDecl(display, kdoc, files[fileIndex].path, indexInFile++))
        }
    }
    return result
}

/**
 * Harvests descriptions from the KDoc of the `@Serializable` spec sources under [root].
 * The returned keys match the display names used by [renderInput]:
 * - `"<Type>"` — the class summary (prose before the first `@` tag).
 * - `"<Type>.<field>"` — from `@property <field> <text>` tags. Empty tags are ignored.
 */
private fun parseKdoc(root: File?): Map<String, String> {
    val descriptions = LinkedHashMap<String, String>()
    for (decl in scanDeclarations(root)) {
        if (decl.kdoc.isBlank()) continue
        val (summary, properties) = parseKdocBlock(decl.kdoc)
        if (summary.isNotBlank()) descriptions.putIfAbsent(decl.display, summary)
        for ((field, description) in properties) descriptions.putIfAbsent("${decl.display}.$field", description)
    }
    return descriptions
}

/**
 * Where each type is declared in the source, so [renderInput] can present types in source order rather
 * than in descriptor-discovery order.
 *
 * @property fileOf display name → path of the source file that declares it.
 * @property indexInFile display name → its declaration index within that file (parents precede their variants).
 */
private class SourceOrder(
    val fileOf: Map<String, String>,
    val indexInFile: Map<String, Int>,
)

/** Harvests the source declaration order of every spec type under [root]. */
private fun parseSourceOrder(root: File?): SourceOrder {
    val fileOf = LinkedHashMap<String, String>()
    val indexInFile = LinkedHashMap<String, Int>()
    for (decl in scanDeclarations(root)) {
        if (fileOf.putIfAbsent(decl.display, decl.file) == null) {
            indexInFile[decl.display] = decl.indexInFile
        }
    }
    return SourceOrder(fileOf, indexInFile)
}

/**
 * Given the source right after a type name, returns the names of its supertypes.
 * Skips a balanced primary-constructor `(...)` first, then reads the `: A, B` clause.
 */
private fun extractSupertypes(after: String): List<String> {
    var i = 0
    while (i < after.length && after[i].isWhitespace()) i++

    if (i < after.length && after[i] == '(') {
        var depth = 0
        do {
            when (after[i]) {
                '(' -> depth++
                ')' -> depth--
            }
            i++
        } while (i < after.length && depth > 0)
    }

    while (i < after.length && after[i].isWhitespace()) i++
    if (i >= after.length || after[i] != ':') return emptyList()
    i++

    val clause = StringBuilder()
    var depth = 0
    while (i < after.length) {
        val c = after[i]
        if (c == '{') break
        if (c == '(') depth++
        if (c == ')') depth--
        if (c == '\n' && depth == 0 && clause.isNotBlank()) break
        clause.append(c)
        i++
    }
    return clause.toString().split(',').mapNotNull { part ->
        Regex("""\s*([A-Za-z_]\w*)""").find(part)?.groupValues?.get(1)
    }
}

/** Splits a raw KDoc block into its summary paragraph and `@property` field descriptions. */
private fun parseKdocBlock(raw: String): Pair<String, Map<String, String>> {
    val lines =
        raw
            .removePrefix("/**")
            .removeSuffix("*/")
            .lines()
            .map { it.trim().removePrefix("*").trim() }

    val summary = StringBuilder()
    val properties = LinkedHashMap<String, String>()
    var current: String? = null
    val propertyTag = Regex("""^@property\s+(\w+)\s*(.*)$""")

    for (line in lines) {
        val tag = propertyTag.find(line)
        when {
            tag != null -> {
                current = tag.groupValues[1]
                properties[current] = tag.groupValues[2].trim()
            }
            line.startsWith("@") -> current = null // some other tag: stop appending
            current != null && line.isNotBlank() -> properties[current] = "${properties[current]} $line".trim()
            current == null && line.isNotBlank() -> summary.append(if (summary.isEmpty()) "" else " ").append(line)
        }
    }
    return cleanKdoc(summary.toString().trim()) to properties.filterValues { it.isNotBlank() }.mapValues { cleanKdoc(it.value) }
}

/** Turns KDoc `[symbol]` references (bracket not followed by a Markdown `(link)`) into inline code so they render cleanly. */
private fun cleanKdoc(text: String): String = text.replace(Regex("""\[([^\]]+)](?!\()"""), "`$1`")
